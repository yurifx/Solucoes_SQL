SELECT CL.CODFIL, CL.NUMTIT, CL.DESDTIT, CL.NRDOCDEVOL, CT.NUMRECDEVOL, CL.TPCLICRC, CL.CODCLICRC, 
       CE.DESCRICAO, CE.DESCRES, CO.DESCRICAO, CO.DESCRES, CL.DTMOVIM, CL.DTCONTABIL, CL.VLLANCTO, 
       CL.OBS, TRUNC (CL.TSULTALT), CL.VLLANCTOPREV
FROM CRC_LANCTO CL
INNER JOIN CRC_TITULO CT ON CT.CODFIL = CL.CODFIL
                         AND CT.NUMTIT = CL.NUMTIT
                         AND CT.DESDTIT = CL.DESDTIT
                         AND CT.TPCLICRC = CL.TPCLICRC
INNER JOIN CRC_EVENTO CE ON CE.CODEVECRC = CL.CODEVECRC 
INNER JOIN CRC_ORIGEM CO ON CO.CODORIGCRC = CL.CODORIGCRC
WHERE CT.NUMRECDEVOL IN (9556478)